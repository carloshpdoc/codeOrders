namespace CodeOrders\V1\Rest\Orders;

use ZF\ApiProblem\ApiProblem;
use ZF\Rest\AbstractResourceListener;

class OrdersResource extends AbstractResourceListener
{

    /**
     * @var OrdersRepository
     */
    private $repository;
    /**
     * @var OrdersServices
     */
    private $services;

    public function __construct(OrdersRepository $repository, OrdersServices $services)
    {
        $this->repository = $repository;
        $this->services = $services;
    }

    /**
     * Create a resource
     *
     * @param  mixed $data
     * @return ApiProblem|mixed
     */
    public function create($data)
    {
        $userRole = $this->repository->findByUsername($this->getIdentity()->getRoleId());

        if($userRole!="salesman"){
            return new ApiProblem(403,'Access only for salesman. You role is : '.$userRole);
        }


        $result = $this->services->insert($data);

        if($result=="error"){
            return new ApiProblem(405, 'Error processing order');
        }

        return $result;
    }

